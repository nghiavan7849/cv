    private String layMaDV(String id) {
        List<DonVi> list = dvdao.selectAll();
        String ma = id;
        if (list.isEmpty()) {
            ma = ma + "01";
        } else {
            String maBCuoiList = list.get(list.size() - 1).getMaDV();
            int sauMaDV = Integer.valueOf(maBCuoiList.substring(2, maBCuoiList.length())) + 1;
            if (sauMaDV < 10) {
                ma = ma + "0" + sauMaDV;
            } else {
                ma = ma + sauMaDV;
            }
        }
        return ma;
    }

