    private void init() {
        this.fillAllTable();
        this.row = -1;
        this.rowUpdate = -1;
        this.updateStatus();

        setBorderInput();
        focusInput();
    }

    void insert() {
        if (checkValidateForm()) {
            DonVi nv = getForm();
            try {
                dvdao.insert(nv);
                this.fillAllTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công!", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                //throw new RuntimeException(e);
                MsgBox.alert(this, "Thêm mới thất bại", JOptionPane.WARNING_MESSAGE);
            }

        }

    }

    void update() {
        if (checkValidateForm()) {
            DonVi dv = getForm();
            try {
                dvdao.update(dv);
                this.fillAllTable();
                MsgBox.alert(this, "Cập nhật thành công!", JOptionPane.INFORMATION_MESSAGE);
                this.clearForm();
            } catch (Exception e) {
                //throw new RuntimeException(e);
                MsgBox.alert(this, "Cập nhật thất bại", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa đơn vị!", JOptionPane.WARNING_MESSAGE);
        } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa đơn vị này?")) {
            String maDV = txtMaDV.getText();
            if(maDV.equals("DV01")){ 
                MsgBox.alert(this, "Bạn không có quyền xóa đơn vị! "+maDV, JOptionPane.WARNING_MESSAGE);
            } else { 
                try {
                dvdao.delete(maDV);
                this.fillAllTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa thành công", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                //throw new RuntimeException(e);
                MsgBox.alert(this, "Xóa thất bại", JOptionPane.WARNING_MESSAGE);
            }
            }    
        }
    }

    void clearForm() {
        DonVi dv = new DonVi();
        this.setForm(dv);
        this.row = -1;
        this.rowUpdate = -1;
        this.updateStatus();
        tblDonVi.clearSelection();

    }

    void edit() {
        String madv = (String) tblDonVi.getValueAt(this.row, 0);
        DonVi dv = dvdao.selectById(madv);
        this.setForm(dv);
        this.updateStatus();
    }

